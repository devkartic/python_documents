# Array print by for loop in python

from array import *

# For number array type integer 'i'
names = array('i', [3, 4, 5, 6, 7])

for e in names:
    print(e, '', end='')

print()

# For character array type unicode 'u'
names = array('u', ['a', 'e', 'i', 'o', 'u'])

for e in names:
    print(e, '', end='')







