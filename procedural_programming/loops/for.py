# For loop in python
# for in range(initialization, termination, increment/decrement). Increment like 1, 2 ,3 , 4, ....

# For loop can handle any data types of sequence
numbers = [2, 4, 'python', 5, 'programming', 8]

for i in numbers:
    print(i, '', end='')
print()

# string with python
string = 'programming'

for i in string:
    print(i, '', end='')
print()

# Range ascending

# for i in range(0, 10):
# for i in range(10):
for i in range(1, 10, 2):
    print(i, '', end='')
print()

# Range descending
for i in range(10, 0, -1):
    print(i, '', end='')






